print("--- Cálculo da Área do Trapézio ---")
base_maior = float(input("Informe o valor da Base Maior: "))
base_menor = float(input("Informe o valor da Base Menor: "))
altura = float(input("Informe o valor da Altura: "))

area_do_trapezio = ((base_maior * base_menor)*altura)/2
print("Área do Trapézio: ",area_do_trapezio)